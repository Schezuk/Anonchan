	# Methods
	public function write($str) {
		return $str;
	}
	public function textTime($time) {
		return date('Y-m-d H:i:s',$time + 3600 * Config::LOCAL_TIMEZONE);
	}
	public function textLike($like){
		return (Config::SHOW_LIKER >= 0) ? self::PREFIX_LIKE . $like . self::SUFFIX_LIKE : '';
	}
	public function textLiker($liker){
		return (Config::SHOW_LIKER >  0) ? self::PREFIX_LIKER . implode(', ', str_split($liker,  MyPdo::LEN_UID)) . self::SUFFIX_LIKER : '';
	}
	public function textDislike($dislike){
		return (Config::SHOW_LIKER >= 0) ? self::PREFIX_DISLIKE . $dislike . self::SUFFIX_DISLIKE : '';
	}
	public function textDisliker($disliker){
		return (Config::SHOW_LIKER >  0) ? self::PREFIX_DISLIKER . implode(', ', str_split($disliker,  MyPdo::LEN_UID)) . self::SUFFIX_DISLIKER : '';	}
	public function textOmission($count){
		return ($count > CONFIG::RECENT_REPLIES) ? self::PREFIX_OMISSION . ($count - CONFIG::RECENT_REPLIES) .self::SUFFIX_OMISSION : '';
	}
	public function errorDetail($obj = NULL){
		return is_null($obj) ? '' : <<< EOT
Request: {$_SERVER['REQUEST_URI']}
Error Code: {$obj->getCode()}
Error File: {$obj->getFile()}
Error Line: {$obj->getLine()}
Error Message: {$obj->getMessage()}
EOT;
	}
}
